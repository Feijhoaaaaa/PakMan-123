        public int[,,] ReadMapMatrixFromJson(string filePath)
        {
            try
            {
                // Check if the file exists
                if (File.Exists(filePath))
                {
                    // Read the JSON content from the file
                    string json = File.ReadAllText(filePath);
                   

                    // Deserialize the JSON content into a 3D array
                    return JsonConvert.DeserializeObject<int[,,]>(json);
                }
                else
                {
                    Debug.WriteLine("File not found.");
                    return null;
                }
            }
            catch (Exception ex)
            {
                // Handle any exceptions (e.g., JSON format errors, file reading errors)
                Debug.WriteLine($"Error reading or deserializing the JSON file: {ex.Message}");
                return null;
            }
        }
        public void PrintToOutput3DArr(int[,,] matrix)
        {
            for (int z = 0; z < matrix.GetLength(0); z++)
            {
                for (int y = 0; y < matrix.GetLength(1); y++)
                {
                    for (int x = 0; x < matrix.GetLength(2); x++)
                    { 
                        Debug.Write($"{matrix[z, y, x]} ");
                    }
                    Debug.WriteLine(" ");
                }
                Debug.WriteLine(" ");
            }
        }

